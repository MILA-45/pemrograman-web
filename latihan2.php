<?php
$a = 5;
$b = 10;

// Operator Increment
$a++; // Setelah operasi ini, nilai variable $a akan menjadi 6
echo "Nilai a setelah increment: " . $a . "<br>";

// Operator Decrement
$b--; // Setelah operasi ini, nilai variable $b akan menjadi 9
echo "Nilai b setelah decrement: " . $b . "<br>";
?>